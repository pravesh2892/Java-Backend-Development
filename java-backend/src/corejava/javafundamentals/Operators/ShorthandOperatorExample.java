package corejava.javafundamentals.Operators;

public class ShorthandOperatorExample {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 7;

        num1 += 3;
        System.out.println(num1);
        System.out.println(num2);

        System.out.println();

        num2 -= 1;
        System.out.println(num1);
        System.out.println(num2);

        System.out.println();

        num1 *= 3;
        System.out.println(num1);
        System.out.println(num2);

        System.out.println();

        num2 /= 4;
        System.out.println(num1);
        System.out.println(num2);

        System.out.println();

        num1 %= 6;
        System.out.println(num1);
        System.out.println(num2);
    }
}
